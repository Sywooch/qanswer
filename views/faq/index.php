		<h3><a name="bounty"></a>如果我没有得到好的答案该怎么办？</h3>
		<p>首先要确定你的问题是不是够好，要想得到最佳的答案，你必须使你的问题明确清晰。对你的问题进行说明改进，这样也会使得你的问题得到更多用户的兴趣。</p>
		<p>如果你认为你已经把你的问题陈述的足够清楚，但是依然没有得到最佳的答案，那么你可以对问题进行悬赏，赏金从<span class="bounty-award">+50</span>到<span class="bounty-award">+500</span>，被悬赏的问题会有专门的页面显示。</p>

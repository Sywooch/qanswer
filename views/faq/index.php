		<h3>在本站点我能问什么问题？</h3>
		<p>本站点为那些钟爱编程的人们而建，你可以在这里提任何在编程中遇到的问题，比如说...</p>
		<ul>
			<li>语法问题</li>
			<li>算法问题</li>
			<li>甚至是编译工具的问题</li>
		</ul>
		<p>需要注意的是，请在提问之前看看是否已经有人已经提过同样的问题。</p>
		<p>你也可以去回答你自己提出的问题。</p>

		<h3>在本站点什么问题我不应该问？</h3>
		<P>本站点不允许提那些过于开放的问题、没有实际意义的问题甚至说是聊天性质的问题。如果你不想你的问题被举报，尽量避免你的问题属于上述情形之列。</P>
		<p>举个例子，如果你想提一个“我想讨论下……”之类的问题，那你不应该在这里提，因为这样的问题过于开放。但是如果你想提一个“我想请教一下……为什么是这样？”之类的问题，那么你可以在这里提，因为你可以得到一个明确的答案。</p>
		<p>如果你的问题是关于本站点本身的，现在你可以在这里提，但是将来我们会做一个专门的站点，让你提出你在本站点所遇到的问题，甚至是bug。</p>

		<h3>我是不是一定需要一个帐号？</h3>
		<p>并不一定需要，你可以匿名浏览所有的问题和答案，并可以对特定的问题进行搜索。但是我们希望你注册一个帐号更方便你提出问题和回答解决别人提出的问题。而且注册后你可以通过提出问题和回答问题来获得威望，从而获得一些在本站点的特权。</p>

		<h3>我该如何提问？</h3>
		<P>使用简洁明确的标题。一个好的标题可以节省回答者的时间，不要用“帮帮忙”（更别说“救命啊！”这样让人反感的话）等对问题没有意义的词语。</P>
		<p>在你的提问内容里，需要提供精确有效的信息。这并不是要求你简单的把大量的出错代码或者数据完全转储摘录到你的提问问题中。如果你有庞大而复杂的测试条件，尽量把它剪裁得越小越好。　</p>
		<p>这样做的好处至少有三点。第一，表现出你为简化问题付出了努力，这可以使你得到回答的机会增加；第二，简化问题使你得到有用答案的机会增加；第三，在提炼 你的bug报告的过程中，也许你自己就能找出问题所在或作出更正。</p>
		<h3>提问后我该做什么?</h3>
		<p>当你在本站发了提问帖之后，在几分钟之内该贴可能就会被其他用户看到并给出一个好的答案，所以你应该经常回来看看你的问题是不是已经很好的得到回答。</p>
		<p>如果你的问题有些东西需要进一步说明，你可以在问题下方添加评论。如果其他用户在评论中希望得到更多的信息，你也可以对你的问题进行编辑。通过以上两种方式，你将可以获得最佳的答案。</p>
		<p>你可以对你觉得有用的答案通过点击答案左侧的向上箭头进行支持投票，支持票数最多的答案一般排在最前列。这样可以使得更多的人看到好的答案。别人也可以对你提出问题中的答案进行投票。</p>
		<p>当你找到你认为的最佳答案是，你可以将其选为“接受答案”，这样可以表明你已经得到了你提出问题的最佳答案，你从本站点获得有价值的信息。</p>


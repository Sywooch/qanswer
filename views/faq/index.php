		<h3>为什么关闭问题？</h3>
		<p>完全脱离主题的问题会被关闭。威望达到3000的用户可每天对问题发出最多50个关闭投票，一旦某个问题的关闭投票达到5票，问题被标记为关闭状态，并且不能接受新的答案。被关闭的问题可以通过重开投票重开。但是一个用户只能对某个问题关闭或重开进行一次投票。</p>

		<h3>为什么某些问题或答案被移除？</h3>
		<p>极度离题或是质量极差的问题会被移除，被关闭的问题如果表明完全无用也会被移除，还有那些很长时间没有答案的问题也可能会被移除。</p>
		<p>和问题完全无关的答案会被移除。例如在答案中问其它的问题，重复的答案或者是“谢谢”之类的答案。如果你想改进的你的答案避免被移除，你可以对你的答案进行编辑。</p>

		<h3>如果发现不好的内容我该怎么办？</h3>
		<p>首先你可以通过举报系统让我们来知道这件事。每个评论和帖子都可以举报，好好使用举报功能。我们尽力维护我们的社区，这也需要你的帮助。你也可以直接发邮件给我们，如果你认为事态极其严重。我们对此深表感谢！</p>

		<h3>其他人可以编辑我的帖子？</h3>
		<p>	是这样的，本站点的任何帖子任何用户都可以编辑，并且可以追溯所有的编辑版本。如果你对于你的问题或答案可以被别人编辑感到不舒服，那么本站点可能不适合你。</p>
		<p>我们希望所有的用户互相尊重、待人以诚。我们在这里相互学习，也要相互容忍。毕竟人各有所长、亦各有其短。永远不要去嘲笑那些你看来很简单的问题。</p>
		<p>我们也希望诚实待人，不要去提供一些误导信息，看到误导信息及时去纠正或是通知我们一起关闭删除。尽我们之所能提供最佳的答案，并对现有的问题和答案持续的编辑改进。</p>
		<p>这是我们共同拥有的社区，共同学习进步的平台，让我们去珍惜去努力以使得这个社区变得更好。</p>

